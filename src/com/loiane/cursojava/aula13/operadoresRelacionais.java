package com.loiane.cursojava.aula13;

public class operadoresRelacionais {
	
	public static void main(String[] args) {
		
		int valor1 = 1;
		int valor2 = 2;
		
		// operadores relacionais
		System.out.println("valor1 é == valor2: " + (valor1 == valor2));
		System.out.println("valor1 é != valor2: " + (valor1 != valor2));
		System.out.println("valor1 é > valor2: " + (valor1 > valor2));
		System.out.println("valor1 é >= valor2: " + (valor1 >= valor2));
		System.out.println("valor1 é < valor2: " + (valor1 < valor2));
		System.out.println("valor1 é <= valor2: " + (valor1 <= valor2));
		
		
	}

}
